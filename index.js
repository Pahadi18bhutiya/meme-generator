async function generate(){
    const api=`https://meme-api.com/gimme/wholesomememes`;
    const data=await fetch(`${api}`).then(Response=>Response.json());
    
    document.querySelector('h4').innerHTML=data.title;
    var image=data.url;
    document.querySelector('img').setAttribute("src",image);
    document.querySelector('h5').innerHTML= "Author: " + data.author;

}
